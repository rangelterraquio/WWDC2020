import Foundation

/**
 This enum it's a interface for the controls keyboard keys for this game.
 */
public enum KeyBoardKeys: UInt16 {
    case a = 0x00
    case w = 0x0D
    case d = 0x02
    case s = 0x01
    case space = 0x31
    case leftArrow = 0x7B
    case rightArrow = 0x7C
}
